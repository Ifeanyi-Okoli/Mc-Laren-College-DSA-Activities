// Push Method
// Create an empty array named fruits
let fruits = [];
// Use the push method push "mango" into the array.
fruits.push("mango");
// Print the fruits array
console.log(fruits);
// Using a single push operation push "apple" & "orange" into the array and store the 
//return value of the operation in a new variable called count
let count = fruits.push("apple", "orange");
console.log(fruits);
console.log(count);
// Print the count variable